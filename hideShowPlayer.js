//////////////////////////hides player /////////////////////////////////////
function hidePlayer() {
    $('#player').hide();
    $('.playerAreaXIconButton').hide();
    $('.playerAreaPencilIconButton').hide();
}
  //////////////////////////show player when clicking an album /////////////////////////////////////
  function showPlayer() {
    $('#player').show();
    $('.playerAreaXIconButton').show();
    $('.playerAreaPencilIconButton').show();
}